import { kv } from '@vercel/kv';

export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

  if (req.method === 'OPTIONS') {
    return res.status(200).end();
  }

  if (req.method !== 'GET') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  try {
    const { investorSlug } = req.query;

    if (!investorSlug) {
      return res.status(400).json({ error: 'investorSlug is required' });
    }

    const storageKey = `investor-profile:${investorSlug}`;
    const storedData = await kv.get(storageKey);

    if (!storedData) {
      return res.status(200).json({
        success: true,
        hasStoredProfile: false,
        profile: null
      });
    }

    return res.status(200).json({
      success: true,
      hasStoredProfile: true,
      profile: storedData.profile,
      verification: storedData.verification,
      generatedAt: storedData.generatedAt
    });

  } catch (error) {
    console.error('Get profile error:', error);
    return res.status(500).json({
      error: 'Failed to retrieve profile',
      details: error.message
    });
  }
}
